    private boolean check(String usuario, String senha){
        if(usuario.equals(user)){
            if(senha.equals(pass)){
                JOptionPane.showMessageDialog(null, "Login Correto", "Login", JOptionPane.INFORMATION_MESSAGE);
                return true;
            }else{
                JOptionPane.showMessageDialog(null, "Senha incorreta", "Login", JOptionPane.WARNING_MESSAGE);
                return false;
            }
        }else{
            JOptionPane.showMessageDialog(null, "Usu√°rio incorreto", "Login", JOptionPane.WARNING_MESSAGE);
            return false;
        }
    }
    
    
