    private void entrarBtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_entrarBtActionPerformed
        // TODO add your handling code here:
        String nome = campoNome.getText();
        String senha = new String(campoSenha.getPassword());
        if(check(nome, senha)){
            new Principal().setVisible(true);
            this.dispose();
        }else{
            campoNome.setText("");
            campoSenha.setText("");
        }
    }//GEN-LAST:event_entrarBtActionPerformed

    private void campoSenhaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_campoSenhaKeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode()== KeyEvent.VK_ENTER && (new String(campoSenha.getPassword()).equals(""))){
            JOptionPane.showMessageDialog(null, "Digite a senha", "Login", JOptionPane.ERROR_MESSAGE);
            campoSenha.requestFocus();
        }else{
            if(evt.getKeyCode()== KeyEvent.VK_ENTER && check(campoNome.getText(), (new String (campoSenha.getPassword())))){
                new Principal().setVisible(true);
                this.dispose();
            }
        }
    }//GEN-LAST:event_campoSenhaKeyPressed

